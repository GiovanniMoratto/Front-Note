/*-------------------------------
	BUTTON 01
-------------------------------*/

var button_01 = document.querySelector("#copyButton01");

button_01.addEventListener('click', function() {

    if (button_01.classList.contains('clipit-success')) {
        button_01.classList.remove('clipit-success');
    } else {
        button_01.classList.add('clipit-success');
        button_01.textContent = "copied";
        setTimeout(function() {
            button_01.textContent = "copy";
            button_01.classList.remove('clipit-success');
            button_01.classList.add('clipit')
        }, 2000)
    }

}, false);

